CBlockTemplate* CreateNewPOSBlockWithKey(CReserveKey& reservekey, CWallet* pwallet)
{
    CPubKey pubkey;
    if (!reservekey.GetReservedKey(pubkey))
        return NULL;

    CScript scriptPubKey = CScript() << ToByteVector(pubkey) << OP_CHECKSIG;
    return CreateNewPOSBlock(scriptPubKey, pwallet);
}

bool ProcessBlockFound2(CBlock* pblock, CWallet& wallet, CReserveKey& reservekey)
{
    LogPrintf("%s\n", pblock->ToString());
    LogPrintf("generated %s\n", FormatMoney(pblock->vtx[0].vout[0].nValue));

    // Found a solution
    {
        LOCK(cs_main);
        if (pblock->hashPrevBlock != chainActive.Tip()->GetBlockHash())
            return error("BCZMiner : generated block is stale");
    }

    // Remove key from key pool
    reservekey.KeepKey();

    // Track how many getdata requests this block gets
    {
        LOCK(wallet.cs_wallet);
        wallet.mapRequestCount[pblock->GetHash()] = 0;
    }

    // Inform about the new block
    GetMainSignals().BlockFound(pblock->GetHash());

    // Process this block the same as if we had received it from another node
    CValidationState state;
    if (!ProcessNewBlock(state, NULL, pblock)) {
        if (pblock->IsZerocoinStake())
            pwalletMain->zbczTracker->RemovePending(pblock->vtx[1].GetHash());
        return error("BCZMiner : ProcessNewBlock, block not accepted");
    }

    for (CNode* node : vNodes) {
        node->PushInventory(CInv(MSG_BLOCK, pblock->GetHash()));
    }

    return true;
}

bool fStake_BCZ = false;
bool fMintableCoins = false;
int nMintableLastCheck = 0;

void POSMiner(CWallet* pwallet, bool fProofOfStake)
{
    LogPrintf("POSMiner started\n");
    SetThreadPriority(THREAD_PRIORITY_LOWEST);
    RenameThread("POSMiner");

    // Each thread has its own key and counter
    CReserveKey reservekey(pwallet);
    unsigned int nExtraNonce = 0;

    while (fStake_BCZ) {
            //control the amount of times the client will check for mintable coins
            if ((GetTime() - nMintableLastCheck > 5 * 60)) // 5 minute check time
            {
                nMintableLastCheck = GetTime();
                fMintableCoins = pwallet->MintableCoins();
            }

            while (vNodes.empty() || pwallet->IsLocked() || !fMintableCoins || (pwallet->GetBalance() > 0 && nReserveBalance >= pwallet->GetBalance()) || !masternodeSync.IsBlockchainSynced())
            {
                nLastCoinStakeSearchInterval = 0;
                // Do a separate 1 minute check here to ensure fMintableCoins is updated
                if (!fMintableCoins) {
                    if (GetTime() - nMintableLastCheck > 1 * 60) // 1 minute check time
                    {
                        nMintableLastCheck = GetTime();
                        fMintableCoins = pwallet->MintableCoins();
                    }
                }
                MilliSleep(10000);
                if (!fStake_BCZ)
                    continue;
            }

            if (mapHashedBlocks.count(chainActive.Tip()->nHeight)) //search our map of hashed blocks, see if bestblock has been hashed yet
            {
                if (GetTime() - mapHashedBlocks[chainActive.Tip()->nHeight] < max(pwallet->nHashInterval, (unsigned int)1)) // wait half of the nHashDrift with max wait of 3 minutes
                {
                    MilliSleep(10000);
                    continue;
                }
            }

        //
        // Create new block
        //
        unsigned int nTransactionsUpdatedLast = mempool.GetTransactionsUpdated();
        CBlockIndex* pindexPrev = chainActive.Tip();
        if (!pindexPrev)
            continue;

        unique_ptr<CBlockTemplate> pblocktemplate(CreateNewPOSBlockWithKey(reservekey, pwallet));
        if (!pblocktemplate.get())
            continue;

        CBlock* pblock = &pblocktemplate->block;
        IncrementExtraNonce2(pblock, pindexPrev, nExtraNonce);

            LogPrintf("POSMiner : proof-of-stake block found %s \n", pblock->GetHash().ToString().c_str());
            if (pblock->IsZerocoinStake()) {
                //Find the key associated with the zerocoin that is being staked
                libzerocoin::CoinSpend spend = TxInToZerocoinSpend(pblock->vtx[1].vin[0]);
                CBigNum bnSerial = spend.getCoinSerialNumber();
                CKey key;
                if (!pwallet->GetZerocoinKey(bnSerial, key)) {
                    LogPrintf("%s: failed to find zBCZ with serial %s, unable to sign block\n", __func__, bnSerial.GetHex());
                    continue;
                }

                //Sign block with the zBCZ key
                if (!SignBlockWithKey(*pblock, key)) {
                    LogPrintf("POSMiner(): Signing new block with zBCZ key failed \n");
                    continue;
                }
            } else if (!SignBlock(*pblock, *pwallet)) {
                LogPrintf("POSMiner(): Signing new block with UTXO key failed \n");
                continue;
            }

            LogPrintf("POSMiner : proof-of-stake block was signed %s \n", pblock->GetHash().ToString().c_str());
            SetThreadPriority(THREAD_PRIORITY_NORMAL);
            ProcessBlockFound2(pblock, *pwallet, reservekey);
            SetThreadPriority(THREAD_PRIORITY_LOWEST);

            continue;

        LogPrintf("Running POSMiner with %u transactions in block (%u bytes)\n", pblock->vtx.size(),
            ::GetSerializeSize(*pblock, SER_NETWORK, PROTOCOL_VERSION));

        //
        // Search
        //
        int64_t nStart = GetTime();
        uint256 hashTarget = uint256().SetCompact(pblock->nBits);
        while (true) {
            unsigned int nHashesDone = 0;

            uint256 hash;
            while (true) {
                hash = pblock->GetHash();
                if (hash <= hashTarget) {
                    // Found a solution
                    SetThreadPriority(THREAD_PRIORITY_NORMAL);
                    LogPrintf("POSMiner:\n");
                    LogPrintf("proof-of-work found  \n  hash: %s  \ntarget: %s\n", hash.GetHex(), hashTarget.GetHex());
                    ProcessBlockFound2(pblock, *pwallet, reservekey);
                    SetThreadPriority(THREAD_PRIORITY_LOWEST);
                    break;
                }
                pblock->nNonce += 1;
                nHashesDone += 1;
                if ((pblock->nNonce & 0xFF) == 0)
                    break;
            }

            // Check for stop or if block needs to be rebuilt
            boost::this_thread::interruption_point();
            // Regtest mode doesn't require peers
            if (vNodes.empty() && Params().MiningRequiresPeers())
                break;
            if (pblock->nNonce >= 0xffff0000)
                break;
            if (mempool.GetTransactionsUpdated() != nTransactionsUpdatedLast && GetTime() - nStart > 60)
                break;
            if (pindexPrev != chainActive.Tip())
                break;
        }
    }
}

void static ThreadPOSMiner(void* parg)
{
    boost::this_thread::interruption_point();
    LogPrintf("ThreadPOSMiner started\n");
    CWallet* pwallet = (CWallet*)parg;
    try {
        POSMiner(pwallet, true);
        boost::this_thread::interruption_point();
    } catch (std::exception& e) {
        LogPrintf("ThreadPOSMiner() exception\n");
    } catch (...) {
        LogPrintf("ThreadPOSMiner() exception...\n");
    }
    LogPrintf("ThreadPOSMiner exiting,\n");
}

void StakeBCZ(bool fStake_BCZ, CWallet* pwallet, int nThreads)
{
    static boost::thread_group* POSMinerThreads = NULL;

    if (nThreads < 0) {
        // In regtest threads defaults to 1
        if (Params().DefaultMinerThreads())
            nThreads = Params().DefaultMinerThreads();
        else
            nThreads = boost::thread::hardware_concurrency();
    }

    if (POSMinerThreads != NULL) {
        POSMinerThreads->interrupt_all();
        delete POSMinerThreads;
        POSMinerThreads = NULL;
    }

    if (nThreads == 0 || !fStake_BCZ)
        return;

    POSMinerThreads = new boost::thread_group();
    for (int i = 0; i < nThreads; i++)
        POSMinerThreads->create_thread(boost::bind(&ThreadPOSMiner, pwallet));
}

